//Letter check using switch case

var letter = prompt("Enter any letter : ");
letter = letter.toLowerCase();

switch(letter) {
     case "a":
       document.write("vowel");
       break;

     case "e":
       document.write("vowel");
       break;

     case "i":
       document.write("vowel");
       break;

     case "o":
       document.write("vowel");
       break;

     case "u":
       document.write("vowel");
       break;

       default:
       document.write("consonant");
}

